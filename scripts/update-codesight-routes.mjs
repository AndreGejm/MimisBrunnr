#!/usr/bin/env node

import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { getHttpRouteDefinitions } from "../apps/mimir-api/dist/server.js";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const args = new Set(process.argv.slice(2));
const write = args.has("--write");
const check = args.has("--check");

if (!write && !check) {
  console.error("Usage: node scripts/update-codesight-routes.mjs --write | --check");
  process.exitCode = 1;
} else {
  await main();
}

async function main() {
  const routes = getHttpRouteDefinitions();
  const routesMarkdown = renderRoutesMarkdown(routes);
  const routesPath = resolve(repoRoot, ".codesight", "routes.md");
  const codesightPath = resolve(repoRoot, ".codesight", "CODESIGHT.md");
  const expectedCodesight = await renderCodesightMarkdown(codesightPath, routesMarkdown, routes.length);

  if (check) {
    const [currentRoutes, currentCodesight] = await Promise.all([
      readFile(routesPath, "utf8"),
      readFile(codesightPath, "utf8")
    ]);
    const mismatches = [];
    if (normalizeNewlines(currentRoutes) !== normalizeNewlines(routesMarkdown)) {
      mismatches.push(".codesight/routes.md");
    }
    if (normalizeNewlines(currentCodesight) !== normalizeNewlines(expectedCodesight)) {
      mismatches.push(".codesight/CODESIGHT.md");
    }
    if (mismatches.length > 0) {
      console.error(`Codesight route artifacts are stale: ${mismatches.join(", ")}`);
      process.exitCode = 1;
    }
    return;
  }

  await mkdir(dirname(routesPath), { recursive: true });
  await Promise.all([
    writeFile(routesPath, routesMarkdown, "utf8"),
    writeFile(codesightPath, expectedCodesight, "utf8")
  ]);
}

function renderRoutesMarkdown(routes) {
  return [
    "# Routes",
    "",
    "> Source of truth: `apps/mimir-api/src/server.ts`",
    "> Generated by: `pnpm codesight:routes`",
    "",
    ...routes.map((route) => {
      const tags = route.tags.length > 0 ? ` [${route.tags.join(", ")}]` : "";
      return `- \`${route.method}\` \`${route.path}\`${tags}`;
    }),
    ""
  ].join("\n");
}

async function renderCodesightMarkdown(codesightPath, routesMarkdown, routeCount) {
  const current = await readFile(codesightPath, "utf8");
  const withSummaryCount = current.replace(
    /> \d+ routes \|/u,
    `> ${routeCount} routes |`
  );
  const routeSection = `${routesMarkdown}\n---\n`;
  return withSummaryCount.replace(
    /# Routes\n[\s\S]*?\n---\n/u,
    routeSection
  );
}

function normalizeNewlines(value) {
  return value.replace(/\r\n/g, "\n");
}
